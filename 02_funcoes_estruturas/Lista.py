lista = [16, 14, 63, 65, 17, 99, 70, 11, 20, 48, 79, 32, 17, 89, 12, 25, 66]

tamanho_lista = len(lista)

maior_numero = max(lista)

menor_numero = min(lista)

soma = sum(lista)

print(f'A lista possui {tamanho_lista} números em que o maior número é {maior_numero} e o menor número é {menor_numero}. A soma dos valores presentes nela é igual à {soma}.')
